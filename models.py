class Product:
    def __init__(self, product_name, product_category, size, price, stock, brand, color, material, release_date):
        self.product_name = product_name
        self.product_category = product_category
        self.size = size
        self.price = price
        self.stock = stock
        self.brand = brand
        self.color = color
        self.material = material
        self.release_date = release_date
